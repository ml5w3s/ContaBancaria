package BEANS;

public class ClienteConta {
        private String clienteConta, valorDeclarado, numConta;

    public String getClienteConta() {
        return clienteConta;
    }

    public void setClienteConta(String clienteConta) {
        this.clienteConta = clienteConta;
    }

    public String getValorDeclarado() {
        return valorDeclarado;
    }

    public void setValorDeclarado(String valorDeclarado) {
        this.valorDeclarado = valorDeclarado;
    }

    public String getNumConta() {
        return numConta;
    }

    public void setNumConta(String numConta) {
        this.numConta = numConta;
    }
}
